COMMON_LETTERS = "ETAOINSHRDLCUMWFGYPBVKJXQZ"

VOWELS = {"A", "E", "I", "O", "U"}

COMMON_DOUBLES = {
    "LL", "EE", "SS", "OO",
    "TT", "RR", "NN", "FF",
    "PP", "MM",
}


# Reward common English words.
def score_dictionary(text):
    score = 0

    for word in text.split():
        if word in COMMON_WORDS:
            score += 20

    return score


# Reward common English bigrams.
def score_bigrams(text):
    score = 0

    for i in range(len(text) - 1):
        bigram = text[i:i + 2]

        if bigram in COMMON_BIGRAMS:
            score += 3

    return score


# Reward common English trigrams.
def score_trigrams(text):
    score = 0

    for i in range(len(text) - 2):
        trigram = text[i:i + 3]

        if trigram in COMMON_TRIGRAMS:
            score += 5

    return score


# Reward common letters.
def score_frequency(text):
    score = 0

    for letter in text:
        if not letter.isalpha():
            continue

        if letter in COMMON_LETTERS:
            score += len(COMMON_LETTERS) - COMMON_LETTERS.index(letter)

    return score


# Reward realistic vowel ratios.
def score_vowels(text):
    letters = [c for c in text if c.isalpha()]

    if not letters:
        return 0

    vowels = sum(1 for letter in letters if letter in VOWELS)

    vowel_ratio = vowels / len(letters)

    if 0.30 <= vowel_ratio <= 0.55:
        return 12

    if vowels == 0:
        return -20

    return 0


# Reward common double letters.
def score_double_letters(text):
    score = 0

    for i in range(len(text) - 1):
        pair = text[i:i + 2]

        if not pair.isalpha():
            continue

        if pair in COMMON_DOUBLES:
            score += 5

        elif pair[0] == pair[1]:
            score -= 2

    return score


# Final English score.
def score_text(text):
    text = text.upper()

    score = 0
    score += score_dictionary(text)
    score += score_bigrams(text)
    score += score_trigrams(text)
    score += score_frequency(text)
    score += score_vowels(text)
    score += score_double_letters(text)

    return score